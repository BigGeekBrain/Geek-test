Hobot-188 снабжен вакуумным насосом, благодаря которому, создается разрежение и робот удерживается на стеклянной поверхности. Мощное всасывание позволяет выдерживать вес до 7 кг при весе робота всего 0.9кг. При этом он является довольно маневренным.

Не стоит беспокоиться о том, что робот может упасть. Робот при работе должен быть постоянно подключен к электрической сети через длинный провод 5 м и адаптер сети. Это обеспечивает продолжительную работу и позволит вам без ожиданий заряда аккумулятора почистить любое количество окон. Кроме того у робота есть резервная батарея (ИБП), которая позволяет ему продолжить удерживаться на окне, даже в случае перебоев с электроэнергией.

Пока он работает, специальный вакуумный датчик, который замеряют силу сцепления с поверхностью и обнаруживают край очищаемой поверхности, такие как край стола или стеклянные двери и т.д. Если сцепления недостаточно, то Хобот остановится и специальным сигналом сообщит о проблеме и поедет в противоположную сторону. Для обеспечение дополнительной безопасности от падения робота на землю используется специальный страховочный шнур, который не позволит ему упасть вниз.

О любой неполадке во время работы hobot дает знать с помощью индикатора и звуковых предупреждений. Так что не нужно беспокоиться, если возникнут какие-то проблемы, он вам сообщит.

Чтобы помыть и отполировать окна с помощью Хобот 188, необходимо лишь нанести небольшое количество моющего средства края чистящих салфеток верхнего и нижнего колеса и включить робота в розетку. По сравнению с обычными тканями, салфетки для HOBOT 188 разработаны из специального волокна «микрофибры», что обеспечивает более качественное поглощение пыли и грязи.

Чистящие салфетки легко снимаются и моются под водой или в стиральной машине.

Роботом можно управлять, для этих целей есть пульт дистанционного управления, которым вы задаете направление движения или включаете автоматическую программу очистки.

